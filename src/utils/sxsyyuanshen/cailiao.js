冷裂壳块（绿）：击败炉壳山鼬后，从其背上取得的背壳的残片。一度燃烧，但此时已经冷却的背壳的裂块，已经失去其中蕴藏的如火的光彩。
蕴热的背壳（蓝）：击败炉壳山鼬后，从其背上取得的背壳的部分结构。炉壳山鼬会在贝壳之后蕴积进食所得的燃素，以备不时之需，燃火的鼬兽在圣山中腾跃时的一抹焰色，或许是长久以来都未曾被人窥得的风景吧。
明燃的棱状壳（紫）：击败炉壳山鼬后，从其背上取得的完整的背壳碎块。据说在一个种群之中，地位越高的炉壳山鼬，其背后蕴藏的燃素会更多，而地位越低越弱的个体，背后蕴藏的燃素越少。形成这个现象的原因也许很好解释：地位越高的炉壳山鼬，需要运用到背后积蓄的燃素的机会或许永远没有地位低的个体那样多吧。

幽雾化形（绿）：在狂猎迷雾中寻得的碎块。如同雪花一般，是由来自异界的漆黑力量显化凝结而成的。
幽雾片甲（蓝）：在狂猎迷雾中寻得的甲片。曾是狂猎魔物所着盔甲的一部分，不知为何没有随着迷雾一同消失。
幽雾兜盔（紫）：在狂猎的迷雾中寻得的兜鍪。或许是因为来自异界的不洁存在汲取了地脉中古老的记忆，造型与失落古国的遗物颇为相似。

失光块骨（绿）：自蕴光的魔兽体表，取下的黯淡骨块。浸润古老月之力的魔兽生长出来的外骨的碎块，已经失去其中蕴藏的隐秘与一度显曜的光亮。
稀光遗骼（蓝）：自蕴光的魔兽体表，取下的散发微光的骨块。仍然残留一部分古老月之力的外骨遗骸，在其中能看到因月之力浸润已久，即使已脱离了本体许久仍无法褪去的深邃颜色。
繁光躯外骸（紫）：自蕴光的魔兽体表，取下的散发着清凉光芒的巨大骨块。曾受古老月之力恩赐的魔兽，能够通过这种的漫长岁月中浸入深髓中的光亮，操纵生长而出的外骨，对它们来说，即使并未完全在生理结构上连接，但浮于体表的外骨仍然是可随心念驱使的身躯的一部分吧。

霜夜的残照（绿）：击败霜夜灵嗣后，从其角冠上取得的残片。传说在遥远的过去，这些高贵的生灵亦是黄金之城的侍仆，辉映于其瞳中的乃是至圣至耀的天光。如今那缕残光早已熄灭，而失却了旧主的灵嗣依然漫行于月下的霜夜，守望无人铭记的誓言。
霜夜的柔辉（蓝）：击败霜夜灵嗣后，从其角冠上取得的碎块。在霜月之子最古老的口头传承祷歌中，这些高贵生灵的辉光曾被终北的哲人称作神使无垢的垂怜。尽管垂怜最终末能阻止黄金城的覆灭，也未能让她的后裔免于流离于荒野的命运，但那柔辉依然流淌在角冠上，等待着也许不会再度降临的梦。
霜夜的煌荣（紫）：击败霜夜灵嗣后，从其角冠上取得的骨甲。其上萦缠着如月光般柔润的温存，即便是在寒夜最深之时亦不会消退。从某种意义上来说，与霜月之子一样，这是这些高贵的生灵最后的坚守。它们侍奉的旧主已经殒入尘埃，它们的梦中却依然辉耀着金色的煌荣。












