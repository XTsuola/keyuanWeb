秘源真芯（紫）：来自不再活动的秘源机兵。看上去是完整的驱动秘源机械体运作的核心，似乎是作为通用的组件而被制造出来的奇特装置。传说古老文明的伟大住民辉用金纹创生的智慧勾勒奇异机械体运作的蓝图，或许这核心也是其中之一吧。

