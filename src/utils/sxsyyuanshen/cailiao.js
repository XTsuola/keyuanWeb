渊光鳍翅（紫）：击败玄文兽后，从其背上取得的华美鳍翅。其上折射着幽邃的微光，能隐隐感受到仙力的痕迹。更深夜静之时，即便是凶暴的野兽亦会短暂沉入宁谧的安眠，追忆【梦】尚不独属于人的年岁。然而曾经创造许多美梦的主人早已不复，昔时的梦亦如沉入渊底的幽光，再也难觅其踪。

残毁的剑柄（绿）：自古老雕像上取得的石制剑柄。在海水的冲刷下已彻底残毁，失去了曾经的光泽。
裂断的剑柄（蓝）：自古老雕像上取得的石制剑柄。剑柄上的断口与刻痕似乎记录了其主人曾经历过的残酷战斗。
未熄的剑柄（紫）：自古老雕像上取得的石制剑柄。或许是因为刚刚离开其主人的缘故，剑柄中的火光还尚未熄灭。

