天门中断楚江开，碧水东流至此回。两岸青山相对出，孤帆一片日边来。

《望月怀远》——张九龄（唐）
海上生明月，天涯共此时。情人怨遥夜，竟夕起相思。灭烛怜光满，披衣觉露滋。不堪盈手赠，还寝梦佳期。

