《桃花庵歌》——唐寅（明）
桃花坞里桃花庵，桃花庵里桃花仙；桃花仙人种桃树，又摘桃花换酒钱。
酒醒只来花前坐，酒醉还来花下眠。半醒半醉日复日，花落花开年复年。
但愿老死花酒间，不愿鞠躬车马前。车尘马足贵者趣，酒盏花枝贫者缘。
若将富贵比贫者，一在平地一在天。若将花酒比车马，他得驱驰我得闲。
世人笑我忒风骚，我笑他人看不穿。不见五陵豪杰墓，无花无酒锄作田。

《滕王阁诗》——王勃（唐）
滕王高阁临江渚，佩玉鸣鸾罢歌舞。画栋朝飞南浦云，珠帘暮卷西山雨。闲云潭影日悠悠，物换星移几度秋。阁中帝子今何在？槛外长江空自流。

《天净沙（秋思）》——马致远（元）
枯藤老树昏鸦，小桥流水人家，古道西风瘦马。夕阳西下，断肠人在天涯。


W
《望庐山瀑布》——李白（唐）
日照香炉生紫烟，遥看瀑布挂前川。飞流直下三千尺，疑是银河落九天。

《望天门山》——李白（唐）
天门中断楚江开，碧水东流至此回。两岸青山相对出，孤帆一片日边来。


X
《夏日绝句》——李清照（宋）
生当作人杰，死亦为鬼雄。至今思项羽，不肯过江东。

《相思》——王维（唐）
红豆生南国，春来发几枝。愿君多采撷，此物最相思。

《小池》——杨万里（宋）
泉眼无声惜细流，树阴照水爱晴柔。小荷才露尖尖角，早有蜻蜓立上头。

《小儿垂钓》——胡令能（唐）
蓬头稚子学垂纶，侧坐莓苔草映身。路人借问遥招手，怕得鱼惊不应人。

《晓出净慈寺送林子方》——杨万里（宋）
毕竟西湖六月中，风光不与四时同。接天莲叶无穷碧，映日荷花别样红。

《宣州谢眺楼饯别校书叔云》——李白（唐）
弃我去者，昨日之日不可留。乱我心者，今日之日多烦忧。
长风万里送秋雁，对此可以酣高楼。蓬莱文章建安骨，中间小谢又清发。俱怀逸兴壮思飞，欲上青天揽明月。抽刀断水水更流，举杯消愁愁更愁。人生在世不称意，明朝散发弄扁舟。

《寻隐者不遇》——贾岛（唐）
松下问童子，言师采药去。只在此山中，云深不知处。


Y
《雁门太守行》——李贺（唐）
黑云压城城欲摧，甲光向日金鳞开。角声满天秋色里，塞上燕脂凝夜紫。半卷红旗临易水，霜重鼓寒声不起。报君黄金台上意，提携玉龙为君死。

《夜书所见》——叶绍翁（宋）
萧萧梧叶送寒声，江上秋风动客情。知有儿童挑促织，夜深篱落一灯明。

《忆江南》——白居易（唐）
江南好，风景旧曾谙。日出江花红胜火，春来江水绿如蓝。能不忆江南？

《忆秦娥（娄山关）》——毛泽东（近现代）
西风烈，长空雁叫霜晨月。霜晨月，马蹄声碎，喇叭声咽。
雄关漫道真如铁，而今迈步从头越。从头越，苍山如海，残阳如血。

《咏鹅》——骆宾王（唐）
鹅鹅鹅，曲项向天歌。白毛浮绿水，红掌拨清波。

《咏柳》——贺知章（唐）
碧玉妆成一树高，万条垂下绿丝绦。不知细叶谁裁出，二月春风似剪刀。

《游子吟》——孟郊（唐）
慈母手中线，游子身上衣。临行密密缝，意恐迟迟归。谁言寸草心，报得三春晖。

《元日》——王安石（宋）
爆竹声中一岁除，春风送暖入屠苏。千门万户曈曈日，总把新桃换旧符。


Z
《早春呈水部张十八员外（其一）》——韩愈（唐）
天街小雨润如酥，草色遥看近却无。最是一年春好处，绝胜烟柳满皇都。

《早发白帝城》——李白（唐）
朝辞白帝彩云间，千里江陵一日还。两岸猿声啼不住，轻舟已过万重山。

《赠汪伦》——李白（唐）
李白乘舟将欲行，忽闻岸上踏歌声。桃花潭水深千尺，不及汪伦送我情。

《舟过安仁》——杨万里（宋）
一叶渔舟两小童，收篙停棹坐船中。怪生无雨都张伞，不是遮头是使风。






